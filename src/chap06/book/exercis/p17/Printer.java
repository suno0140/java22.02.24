package chap06.book.exercis.p17;

public class Printer {

	public static void prinln(int i) {
		System.out.println(i);
		
	}

	public static void prinln(boolean b) {
		System.out.println(b);
		
	}

	public static void prinln(double d) {
		System.out.println(d);
		
	}

	public static void prinln(String string) {
		System.out.println(string);
		
	}

}
